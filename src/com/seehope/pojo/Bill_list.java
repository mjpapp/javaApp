package com.seehope.pojo;

public class Bill_list {
	private int bill_id;
	private String user_phone;
	private String bill_beCalled;
	private int bill_time;
	private double bill_money;
	public int getBill_id() {
		return bill_id;
	}
	public void setBill_id(int bill_id) {
		this.bill_id = bill_id;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getBill_beCalled() {
		return bill_beCalled;
	}
	public void setBill_beCalled(String bill_beCalled) {
		this.bill_beCalled = bill_beCalled;
	}
	public int getBill_time() {
		return bill_time;
	}
	public void setBill_time(int bill_time) {
		this.bill_time = bill_time;
	}
	public double getBill_money() {
		return bill_money;
	}
	public void setBill_money(double bill_money) {
		this.bill_money = bill_money;
	}
	public Bill_list() {
		
	}

	public String toString() {
		return "Bill_list [bill_id=" + bill_id + ", user_phone=" + user_phone + ", bill_beCalled=" + bill_beCalled
				+ ", bill_time=" + bill_time + ", bill_money=" + bill_money + "]";
	}
	
	
}
