package com.seehope.pojo;

public class Supperlier_list {
	private String sup_id;
	private String rate_name;
	private String sup_phone;
	public String getSup_id() {
		return sup_id;
	}
	public void setSup_id(String sup_id) {
		this.sup_id = sup_id;
	}
	public String getRate_name() {
		return rate_name;
	}
	public void setRate_name(String rate_name) {
		this.rate_name = rate_name;
	}
	public String getSup_phone() {
		return sup_phone;
	}
	public void setSup_phone(String sup_phone) {
		this.sup_phone = sup_phone;
	}
	public Supperlier_list() {
		
	}
	public String toString() {
		return "Supperlier_list [sup_id=" + sup_id + ", rate_name=" + rate_name + ", sup_phone=" + sup_phone + "]";
	}
	

}
