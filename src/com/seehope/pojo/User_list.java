package com.seehope.pojo;

public class User_list {
	private String user_phone;
	private String user_adress;
	private String user_bankNum;
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getUser_adress() {
		return user_adress;
	}
	public void setUser_adress(String user_adress) {
		this.user_adress = user_adress;
	}
	public String getUser_bankNum() {
		return user_bankNum;
	}
	public void setUser_bankNum(String user_bankNum) {
		this.user_bankNum = user_bankNum;
	}
	public User_list() {
		
	}
	@Override
	public String toString() {
		return "User_list [user_phone=" + user_phone + ", user_adress=" + user_adress + ", user_bankNum=" + user_bankNum
				+ "]";
	}


}
