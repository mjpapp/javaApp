package com.seehope.pojo;

public class Rate_list {
	private String rate_name;
	private String rate_standard;
	public String getRate_name() {
		return rate_name;
	}
	public void setRate_name(String rate_name) {
		this.rate_name = rate_name;
	}
	public String getRate_standard() {
		return rate_standard;
	}
	public void setRate_standard(String rate_standard) {
		this.rate_standard = rate_standard;
	}
	public Rate_list() {
		
	}

	public String toString() {
		return "Rate_list [rate_name=" + rate_name + ", rate_standard=" + rate_standard + "]";
	}
	
}
